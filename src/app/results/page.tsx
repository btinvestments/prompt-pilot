"use client";

import { useState, useEffect } from "react";
import { useRouter, useSearchParams } from "next/navigation";
import { toast } from "sonner";

import { Button } from "@/components/ui/button";
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { Separator } from "@/components/ui/separator";

export default function ResultsPage() {
  const router = useRouter();
  const searchParams = useSearchParams();
  const promptId = searchParams.get("id");
  
  const [isLoading, setIsLoading] = useState(true);
  const [result, setResult] = useState<{
    prompt: string;
    response: string;
    model: string;
    tokens: number;
  } | null>(null);

  useEffect(() => {
    // If there's no prompt ID, redirect to the new prompt page
    if (!promptId) {
      router.push("/prompt/new");
      return;
    }

    // Fetch the result data
    async function fetchResult() {
      try {
        const response = await fetch(`/api/prompt/result?id=${promptId}`);
        
        if (!response.ok) {
          throw new Error("Failed to fetch result");
        }
        
        const data = await response.json();
        setResult(data);
      } catch (error) {
        console.error("Error fetching result:", error);
        toast.error("Failed to load result. Please try again.");
      } finally {
        setIsLoading(false);
      }
    }

    fetchResult();
  }, [promptId, router]);

  if (isLoading) {
    return (
      <div className="container py-8 flex justify-center items-center min-h-[60vh]">
        <div className="text-center">
          <h2 className="text-2xl font-bold mb-4">Loading result...</h2>
          <p className="text-muted-foreground">Please wait while we fetch your result.</p>
        </div>
      </div>
    );
  }

  if (!result) {
    return (
      <div className="container py-8 flex justify-center items-center min-h-[60vh]">
        <div className="text-center">
          <h2 className="text-2xl font-bold mb-4">Result not found</h2>
          <p className="text-muted-foreground mb-6">The result you're looking for could not be found.</p>
          <Button onClick={() => router.push("/prompt/new")}>Create New Prompt</Button>
        </div>
      </div>
    );
  }

  return (
    <div className="container py-8">
      <h1 className="text-3xl font-bold mb-6">AI Response</h1>
      
      <div className="grid gap-6 md:grid-cols-2">
        <Card>
          <CardHeader>
            <CardTitle>Your Prompt</CardTitle>
            <CardDescription>
              The prompt you sent to the AI model.
            </CardDescription>
          </CardHeader>
          <CardContent>
            <div className="p-4 border rounded-md bg-muted">
              <p className="whitespace-pre-wrap">{result.prompt}</p>
            </div>
            <div className="mt-4 text-sm text-muted-foreground">
              <p>Model: {result.model}</p>
              <p>Tokens used: {result.tokens}</p>
            </div>
          </CardContent>
          <CardFooter>
            <Button variant="outline" onClick={() => {
              navigator.clipboard.writeText(result.prompt);
              toast.success("Prompt copied to clipboard!");
            }}>
              Copy Prompt
            </Button>
          </CardFooter>
        </Card>
        
        <Card>
          <CardHeader>
            <CardTitle>AI Response</CardTitle>
            <CardDescription>
              The response generated by the AI model.
            </CardDescription>
          </CardHeader>
          <CardContent>
            <div className="p-4 border rounded-md bg-muted">
              <p className="whitespace-pre-wrap">{result.response}</p>
            </div>
          </CardContent>
          <CardFooter className="flex justify-between">
            <Button variant="outline" onClick={() => {
              navigator.clipboard.writeText(result.response);
              toast.success("Response copied to clipboard!");
            }}>
              Copy Response
            </Button>
            <Button onClick={() => router.push("/prompt/new")}>
              Create New Prompt
            </Button>
          </CardFooter>
        </Card>
      </div>
      
      <div className="mt-8">
        <h2 className="text-2xl font-bold mb-4">What would you like to do next?</h2>
        <div className="flex flex-wrap gap-4">
          <Button onClick={() => router.push("/prompt/new")}>
            Create Another Prompt
          </Button>
          <Button variant="outline" onClick={() => router.push("/history")}>
            View Prompt History
          </Button>
          <Button variant="outline" onClick={() => router.push("/models")}>
            Browse AI Models
          </Button>
        </div>
      </div>
    </div>
  );
}
